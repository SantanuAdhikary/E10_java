class Increment  
{
	public static void main(String[] args) 
	{
		int a = 20 ; 
		int b = ++a ; 
		
		System.out.println("a value is : "+a);
		System.out.println("b value is : "+b);
		
		int c = 20 ; 
		int d = c++;
		
		System.out.println("c value is : "+c);
		System.out.println("d value is : "+d);
		
		
	}
}
