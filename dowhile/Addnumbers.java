class Addnumbers 
{
	public static void main(String[] args) 
	{
		
		System.out.println("---------------while loop-----------------");
		
		int i= 10; 
		
		while(i<=5)
		{
			System.out.println(i);
			i++;
		}
		
		
		System.out.println("---------------do while loop-----------------");
		
		int j = 10 ; 
		
		do{
			
			System.out.println(j);
			j++;
		}while(j<=5);
	}
}
